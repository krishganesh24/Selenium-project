package week1day2;

public class SwitchForArithmatic {

	public static void main(String[] args) {

		String opr="ADD";
		int a=10,b=20;

		switch(opr)
		{
	case("ADD"):
		System.out.println(a+b);
	break;

	case("SUB"):
		System.out.println(a-b);
	break;

	case ("multiple"):

	System.out.println(a*b);
	break;
	case ("Div"):
		
		System.out.println(a/b);
		
		break;

	}

	}		
	}
