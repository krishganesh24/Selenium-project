package week1day1homeowork;

public class RelationalAndLogicalOperators {

	public static void main(String[] args) {

		int a=10;
		int b=12;
		int c=15;
		
		System.out.println(c>b && c>a);
		System.out.println(a>b || c>a);

		System.out.println(++a + ++b);
		System.out.println(++a - ++b);
		
	}
	}
